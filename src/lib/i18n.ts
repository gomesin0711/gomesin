// Get listing specs in the current language (fallback to Indonesian if translation missing)
export function listingSpecs(listing: { specs: any; specsEn?: string | null; specsZh?: string | null }, lang: Lang): Record<string, string> {
  try {
    const parse = (v: any): Record<string, string> => {
      if (!v) return {};
      if (typeof v === "string") return JSON.parse(v);
      return v as Record<string, string>;
    };
    if (lang === "en" && listing.specsEn) return parse(listing.specsEn);
    if (lang === "zh" && listing.specsZh) return parse(listing.specsZh);
    return parse(listing.specs);
  } catch {
    return {};
  }
}
