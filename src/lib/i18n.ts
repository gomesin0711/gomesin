// Translate a category name from DB (Indonesian) to the current language
export function categoryName(name: string, lang: Lang): string {
  if (lang === "id") return name;
  const entry = CATEGORY_NAME_MAP[name];
  if (!entry) return name;
  return entry[lang] ?? name;
}

// Get listing title in the current language (fallback to Indonesian if translation missing)
